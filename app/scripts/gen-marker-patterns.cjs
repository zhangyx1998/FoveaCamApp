// Dev-time generator (docs/history/refactor/orchestrator.md §7.1 Stage 3 T1) — dumps
// the ArUco/AprilTag dictionary bit-grids `src/graphics/Marker.vue` needs to
// *draw* a marker pattern into a static, committed TS module, so that
// component no longer needs to construct a real `core/Vision` MarkerDetector
// at runtime just to look up a pattern (the last renderer-reachable `core`
// dependency — see the §7.1 S1c writeup). Runs in plain Node, not bundled
// into the renderer, so it's free to `require("core")` directly.
//
// Only "4X4_50" is generated: grepped every `<Marker>`/`Marker.vue`
// `:dictionary` usage across the tree (calibrate-drift/-distortion/
// -extrinsic's `RemoteCanvasTeleport` displays, plus the DEV-only
// `playground/`) and none ever pass a `dictionary` prop — every call site
// relies on `Marker.vue`'s own "4X4_50" default. Re-run this script (and
// extend `DICTIONARIES` below) if a caller ever needs a different one.
//
// Usage: node scripts/gen-marker-patterns.cjs (from `app/`)

const { writeFileSync } = require("fs");
const { resolve } = require("path");
const { MarkerDetector } = require("core/Vision");

/** dictionary name -> how many predefined marker ids it has. */
const DICTIONARIES = {
  "4X4_50": 50,
};

const OUT_PATH = resolve(__dirname, "../lib/marker-patterns.generated.ts");

function generate() {
  /** @type {Record<string, (0|1)[][][]>} */
  const byDictionary = {};
  for (const [name, count] of Object.entries(DICTIONARIES)) {
    const detector = new MarkerDetector(name);
    const patterns = [];
    for (let id = 0; id < count; id++) {
      const p = detector.pattern(id);
      patterns.push(Array.from(p, (row) => Array.from(row, (v) => (v ? 1 : 0))));
    }
    byDictionary[name] = patterns;
  }
  return byDictionary;
}

function render(byDictionary) {
  const lines = [
    "// ------------------------------------------------------",
    "// Copyright (c) 2025 Yuxuan Zhang, dev@z-yx.cc",
    "// This source code is licensed under the MIT license.",
    "// You may find the full license in project root directory.",
    "// -------------------------------------------------------",
    "//",
    "// AUTO-GENERATED by `app/scripts/gen-marker-patterns.cjs` — do not edit by",
    "// hand; re-run that script instead. See its header for why this exists",
    "// (docs/history/refactor/orchestrator.md §7.1 Stage 3 T1).",
    "",
    "/** `[dictionary][id]` -> the marker's bit grid (`1` = white cell). */",
    "export const MARKER_PATTERNS: Readonly<Record<string, readonly (readonly (0 | 1)[])[][]>> =",
    JSON.stringify(byDictionary, null, 2) + ";",
    "",
  ];
  return lines.join("\n");
}

const byDictionary = generate();
writeFileSync(OUT_PATH, render(byDictionary), "utf-8");
const counts = Object.entries(byDictionary)
  .map(([k, v]) => `${k} (${v.length})`)
  .join(", ");
console.log(`Wrote ${OUT_PATH.replace(process.cwd(), ".")}: ${counts}`);
